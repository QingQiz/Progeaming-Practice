#include<stdio.h>
int main()
{
	double b,a;
	scanf("%lf",&a);
	if(a<=10)
		b=0.1*a;
	else if(a<=20)
		b=0.1*10+0.075*(a-10);
	else if(a<=40)
		b=0.1*10+0.075*10+0.05*(a-20);
	else if(a<=60)
		b=0.1*10+0.075*10+0.05*20+0.03*(a-40);
	else if(a<=100)
		b=0.1*10+0.075*10+0.05*20+0.03*20+0.015*(a-60);
	else if(a>100)
		b=0.1*10+0.075*10+0.05*20+0.03*20+0.015*40+0.01*(a-100);
	printf("%.6lf",b);
	return 0;
}
